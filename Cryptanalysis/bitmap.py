# Convert data to a list of lists of pixels
pixel_data = []
for line in data:
    pixels = []
    for segment in line.split('+'):
        color, length = segment.split('x')
        color = int(color)
        length = int(length)
        pixels.extend([color] * length)
    pixel_data.append(pixels)

# Find the maximum width of the image
max_width = max(len(row) for row in pixel_data)

# Normalize the width by padding with white (0)
for row in pixel_data:
    row.extend([0] * (max_width - len(row)))

# Convert pixel data to an image
height = len(pixel_data)
image = Image.new('1', (max_width, height))  # '1' for 1-bit pixels (black and white)
for y, row in enumerate(pixel_data):
    for x, pixel in enumerate(row):
        image.putpixel((x, y), pixel)

# Save the image
image.save('output_image.png')

